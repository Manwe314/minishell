#include "libft.h"

int	ft_isalnum(int c)
{
	if (c > 47 && c < 58)
	{
		if (c == 48)
			return (1);
		return (c);
	}
	if ((c > 64 && c < 91) || (c > 96 && c < 123))
		return (c);
	return (0);
}
